#include<iostream>
using namespace std;
int main(){
    // increment and decrement
    //this is our comment
    int x = 5;
    cout<<x<<endl;
    x++; // post increment -> x = x+1
    cout<<++x;   
    ++x; // pre increment
    cout<<endl;
    cout<<x++<<endl;
    cout<<x<<endl;
    int y = 11*7;
    cout<<y<<endl;
    y--; // post increment
    cout<<y<<endl;
    cout<<y--<<endl;
    cout<<--y<<endl;
}